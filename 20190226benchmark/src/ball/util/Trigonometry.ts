// 三角関数クラス
export class Trigonometry{
	public c: number;
	public s: number;
	public constructor(r: number){
		this.set(r);
	}
	public set(r: number): void{
		this.c = Math.cos(r);
		this.s = Math.sin(r);
	}
}

